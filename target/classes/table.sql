create table patient(
patient_id  BIGSERIAL primary key,
first_name varchar(255),
middle_name varchar(255),
last_name varchar(255),
dob date,
age integer,
country_code varchar(255),
phone_number varchar(255),
race_id integer,
race_ethnicity varchar(255),
sub_race varchar(255),
country_name varchar(255),
island_name varchar(255),
father_island varchar(255),
mother_island varchar(255),
num_of_pregnancies integer,
first_pregnancy_age integer,
num_of_children integer,
pregnancy_status_now integer,
breast_feeding_status_now integer,
first_menstrual_period int,
last_menstrual_period varchar(255),
survey_id bigint references survey(survey_id),
created_dt TIMESTAMP WITH TIME ZONE DEFAULT NULL,
updated_dt TIMESTAMP WITH TIME ZONE DEFAULT NULL,
unique (first_name,last_name,phone_number));



